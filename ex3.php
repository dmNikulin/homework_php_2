<?php

$cities = [
    "Московская область" => ["Москва","Зеленоград","Клин"],
    "Ленинградская область" => ["Санкт-Петербург","Всеволожск","Павловск"],
    "Рязанская область" => ["Рязань","Ряжск","Скопин"]
];


foreach ($cities as $key => $obl) {
    echo $key . ":\n";
    $space = "";
    foreach ($obl as $cities) {
        $space .= $cities . ", ";
    }
    echo "$space \n";
}

echo "\n";